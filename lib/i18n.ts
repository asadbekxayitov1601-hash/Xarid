export type MessageKey = keyof (typeof dict)["uz"];

export function t(locale: Locale, key: MessageKey, params?: Record<string, string | number>): string {
  // Fall back to uz for keys that haven't been translated yet — see
  // docs/ACCOUNTS.md section 6 for the discipline rule.
  const fromLocale = (dict as Record<string, Record<string, string>>)[locale]?.[key];
  let s: string = fromLocale ?? (dict.uz as Record<string, string>)[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

export function unitLabel(locale: Locale, unit: string): string {
  const key = `unit_${unit}` as MessageKey;
  const localeDict = (dict as Record<string, Record<string, string>>)[locale] ?? dict.uz;
  return localeDict[key] ? t(locale, key) : unit;
}

export function uzs(locale: Locale, amount: number): string {
  return new Intl.NumberFormat("ru-RU").format(amount) + " " + t(locale, "sum");
}
