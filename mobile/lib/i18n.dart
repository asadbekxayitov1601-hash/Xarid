/// Lightweight app localization (uz / ru / en). The selected locale is persisted
/// in shared_preferences and exposed as a ChangeNotifier so switching the
/// language re-renders every screen that reads a string via `context.t(...)`.
///
/// Uzbek (Latin) is the source/fallback locale: a missing ru/en key falls back
/// to uz, and a missing uz key renders the raw key (so gaps are visible, never a
/// crash). Mirrors the web app's lib/i18n.ts three-locale contract.
class L10n extends ChangeNotifier {
  String _locale = 'uz';
  String get locale => _locale;

  static const supported = ['uz', 'ru', 'en'];
  static const names = {'uz': "O'zbekcha", 'ru': 'Русский', 'en': 'English'};

  Future<void> load() async {
    final prefs = await SharedPreferences.getInstance();
    final saved = prefs.getString('locale');
    if (saved != null && supported.contains(saved)) _locale = saved;
    notifyListeners();
  }

  Future<void> setLocale(String code) async {
    if (!supported.contains(code) || code == _locale) return;
    _locale = code;
    (await SharedPreferences.getInstance()).setString('locale', code);
    notifyListeners();
  }

  String t(String key, [Map<String, String>? params]) {
    var s = _strings[_locale]?[key] ?? _strings['uz']?[key] ?? key;
    if (params != null) params.forEach((k, v) => s = s.replaceAll('{$k}', v));
    return s;
  }
}

extension L10nX on BuildContext {
  /// Localized string that rebuilds the caller on a language change. Use inside
  /// build methods.
  String t(String key, [Map<String, String>? params]) => watch<L10n>().t(key, params);

  /// Localized string without subscribing to changes. Use inside callbacks
  /// (onTap, snackbars) where `watch` isn't allowed.
  String tr(String key, [Map<String, String>? params]) => read<L10n>().t(key, params);

  L10n get l10n => read<L10n>();
}

